def _format_ticks(ax):
    """设置内侧刻度、限制密度并格式化小数。"""
    ax.tick_params(direction='in', which='both', top=True, right=True)
    formatter = FuncFormatter(lambda x, _: f"{x:.1f}")
    ax.xaxis.set_major_formatter(formatter)
    ax.yaxis.set_major_formatter(formatter)
    for axis in [ax.xaxis, ax.yaxis]:
        labels = axis.get_ticklabels()
        if len(labels) > 8:
            for idx, label in enumerate(labels):
                if idx % 2 == 1:
                    label.set_visible(False)

def main():
    """绘制P-R曲线。"""
    fig, ax = plt.subplots(figsize=(6, 8))

    size = 15

    # 设置字体为黑体，解决中文显示问题
    font = "FangZhengShuSong-GBK-1.ttf" # 请确保你的系统中有这个字体文件，或替换为其他可用的中文字体，如 'SimHei'
    try:
        font_prop = FontProperties(fname=font, size=size)
    except IOError:
        print(f"警告：找不到字体文件 '{font}'，将使用默认字体。中文可能无法正常显示。")
        font_prop = FontProperties() # Fallback to default font

    plt.rcParams['axes.unicode_minus'] = False  # 解决负号显示问题

    # --- 使用循环绘制曲线 ---
    for curve in CURVES_CONFIG:
        ax.plot(
            curve['R'],
            curve['P'],
            color=curve['color'],
            marker=curve['marker'],
            linestyle='-',
            label=curve['label'],
            markersize=5,      # 控制标记大小
            markevery=2        # 每隔2个数据点显示一个标记，避免拥挤
        )


    # 设置坐标轴标签
    ax.set_xlabel('查全率', fontproperties=font_prop, fontsize=size)
    ax.set_ylabel('查准率', fontproperties=font_prop, fontsize=size)

    plt.xticks(fontproperties=font_prop, fontsize=size)
    plt.yticks(fontproperties=font_prop, fontsize=size)
    # 格式化刻度
    _format_ticks(ax)

    # 设置图例
    ax.legend(frameon=False, loc='upper right', prop=font_prop)

    # 移除默认标题
    ax.set_title('')

    visible_ticks = {
        "top": False,
        "right": False
    }
    plt.tick_params(axis="x", which="both", **visible_ticks)
    plt.tick_params(axis="y", which="both", **visible_ticks)

    plt.tight_layout()
    plt.savefig("pr_curves.png", dpi=300)
    plt.show()

if __name__ == '__main__':
    main()